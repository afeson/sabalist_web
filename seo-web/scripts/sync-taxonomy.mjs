// Regenerates src/lib/taxonomy.ts + locations.ts from the app's source of truth
// (../src/config/categories.js and ../src/config/cityRoutes.js) so the SEO layer
// never drifts from the app. Run: npm run sync-taxonomy
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const APP = path.resolve(__dirname, '../../src/config');
const OUT = path.resolve(__dirname, '../src/lib');

function parseCategories() {
  const s = fs.readFileSync(path.join(APP, 'categories.js'), 'utf8');
  const topRe = /key:\s*'([^']+)',\s*[\r\n]+\s*id:\s*'([^']+)',[\s\S]*?subCategories:\s*\[([\s\S]*?)\]/g;
  const cats = []; let m;
  while ((m = topRe.exec(s))) {
    const key = m[1], id = m[2];
    const subRe = /id:\s*'([^']+)',\s*labelKey[\s\S]*?fallback:\s*'([^']+)'/g;
    const subs = []; let c;
    while ((c = subRe.exec(m[3]))) subs.push({ id: c[1], name: c[2] });
    cats.push({ id, key, name: key, subCategories: subs });
  }
  return cats;
}

const cats = parseCategories();
const header = `// AUTO-GENERATED by scripts/sync-taxonomy.mjs — do not edit by hand.\n\n`;
const body =
  `export type SubCategory = { id: string; name: string };\n` +
  `export type Category = { id: string; key: string; name: string; subCategories: SubCategory[] };\n\n` +
  `export const CATEGORIES: Category[] = ${JSON.stringify(cats, null, 2)};\n\n` +
  `export const CATEGORY_BY_ID: Record<string, Category> = Object.fromEntries(CATEGORIES.map((c) => [c.id, c]));\n` +
  `export function getCategory(id: string){ return CATEGORY_BY_ID[id]; }\n` +
  `export function getSubCategory(catId: string, subId: string){ return getCategory(catId)?.subCategories.find((s) => s.id === subId); }\n`;
fs.writeFileSync(path.join(OUT, 'taxonomy.ts'), header + body);
console.log(`[sync] wrote taxonomy.ts (${cats.length} categories). NOTE: locations.ts is maintained manually — extend from cityRoutes.js SEO_COUNTRIES.`);
